int	ft_sqrt(int nb)
{
	int	j;

	if (nb < 1)
		return (0);
	if (nb == 1)
		return (1);
	j = 1;
	while (j * j < nb)
		j++;
	if (j > (nb / j))
		return (0);
	return (j);
}
